#include <libft.h>
#include <time.h>

float		ft_rand(void)
{
	static int	seed;

	if (seed == 0)
		seed = time(NULL);
	seed = 214013 * seed + 2531011;
	return (((float)((seed >> 16) & 0x7FFF)) / 32767);
}
